Al cumplirse cien días del ejercicio del control gubernamental durante la emergencia sanitaria, desde la publicación de la Ley N°31016, el presente informe de gestión tiene por finalidad detallar las principales acciones efectuadas por la <a href="http://www.contraloria.gob.pe/"><strong>Contraloría General de la República</strong></a> a las entidades receptoras de recursos públicos durante la pandemia, y que han contribuido a prevenir riesgos de corrupción e inconducta funcional, así como garantizar que los bienes y servicios hayan llegado a los ciudadanos, principalmente de condición más vulnerable. </p>
                                      <p><b>Temas a tratar:</b> <br />
                                        - La estrategia de control desplegada<br />
                                        - Los resultados del control al COVID-19<br />
                                        - La aplicación de las TIC´S en tiempos de COVID-19</p>
