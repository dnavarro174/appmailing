            <td width="582"><p style="font-size: 14px; font-family: arial; text-align: justify">La CAII 2019 es organizada por la Contraloría General de la República y tiene como objetivo principal estimular el debate a través de la creación de un espacio de análisis sobre nuevos enfoques y prácticas que se van generando en la región y a nivel mundial, en temas de política anticorrupción y de integridad, a fin de asimilar las mejores experiencias e intercambiar conocimientos entre representantes de la academia, funcionarios públicos, medios de comunicación, sector privado, sociedad civil, y expertos en la materia, con el fin ulterior de desarrollar nuevas estrategias, cada vez más innovadoras, en la lucha contra la corrupción e inconducta funcional.
